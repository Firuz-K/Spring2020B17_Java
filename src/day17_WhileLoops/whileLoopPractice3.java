package day17_WhileLoops;

public class whileLoopPractice3 {
    public static void main(String[] args) {

        for (int i=0; i<100; i++){
            System.out.print("*");
        }
        System.out.print(" \n");
        int i=0;
        while (i<100){
            System.out.print("*");
            i++;
        }
    }
}
