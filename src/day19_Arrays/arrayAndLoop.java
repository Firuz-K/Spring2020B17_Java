package day19_Arrays;

public class arrayAndLoop {
    public static void main(String[] args) {
        String [] friends={"Mike", "Adam", "Anna", "Tim", "Iman", "Firuz"};
        //        index=  0        1       2      3          4      5

        for (int i=0; i<=5; i++){
            String name=friends[i];
            System.out.print(name+" ");//===> Mike Adam Anna Tim Iman Firuz
        }




    }
}
