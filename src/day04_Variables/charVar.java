package day04_Variables;

public class charVar {

    public static void main(String[] args) {
        char ch1=5;
        int at = 'n';

        System.out.println("1 "+ch1);
        System.out.println("2 "+at);
        System.out.println("3 "+(12>'f'));
        System.out.println("4 "+(10==5+5));
        System.out.println("5 "+("Corona"=="Ebola"));
        System.out.println("6 "+(97=='a'));
        System.out.println("9 "+(!true)); // opposite operator
        System.out.println("10 "+( !(9>10) !=!(10>9)));


    }

}
