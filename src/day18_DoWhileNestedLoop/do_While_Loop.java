package day18_DoWhileNestedLoop;

public class do_While_Loop {

    public static void main(String[] args) {

        int i=3;
        do{
            System.out.println("Hello world");

        }while(i==3);   //  false

        System.out.println("==========================");

        while (i==3){   // false
            System.out.println("Hello");
        }
    }
}
