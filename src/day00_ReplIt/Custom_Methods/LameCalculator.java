package day00_ReplIt.Custom_Methods;

public class LameCalculator {
    int plus;
    int minus;
    int multiply;
    int divide;


    public int plus(int i, int i1) {
        return i+i1;
    }
    public int minus(int i, int i1) {
        return i-i1;
    }
    public int multiply(int i, int i1) {
        return i*i1;
    }
    public int divide(int i, int i1) {
        return i/i1;
    }



}
